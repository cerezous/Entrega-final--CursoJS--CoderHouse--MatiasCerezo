class Bebida {
  constructor(id, nombre, precio, img, stock, id_cat) {
    this.id = id;
    this.nombre = nombre;
    this.precio = precio;
    this.img = img;
    this.stock = stock;
    this.id_cat = id_cat;
  }
}

class Pizza {
  constructor(id, nombre, ingredientes, precio, img, stock, id_cat) {
    this.id = id;
    this.nombre = nombre;
    this.ingredientes = ingredientes;
    this.precio = precio;
    this.img = img;
    this.stock = stock;
    this.id_cat = id_cat;
  }
}